import 'package:flutter/material.dart';

class ColorStyles {
  ColorStyles._();

  static const lightOrange = Color(0xFFFEBD84);
  static const background = Color(0xFFFFF8F2F1);
  static const lightPurple = Color(0xFFE6D3F8);
  static const darkPurple = Color(0xFFB99AE4);
  static const orange = Color(0xFFFD915A);
  static const lightBlue = Color(0xFFE4F2FF);
  static const blue = Color(0xFF98B1FC);
  static const pink = Color(0xFFF38BBB);
  static const darkBlue = Color(0xFF2A458A);
  static const white = Color(0xFFFFFFFF);
  static const black = Color(0xFF000000);
  static const darkOrange = Color(0xFFFF6314);
  static const red = Color(0xFFBD0319);
  static const gray = Color(0xFF5A5252);

  static Color fromCategory(String cate) {
    // enum: ['Học thuật','Tình nguyện', 'Phong trào', 'Văn nghệ'],
    switch (cate) {
      case 'Học thuật':
        return darkPurple;
      case 'Tình nguyện':
        return blue;
      case 'Phong trào':
        return orange;
      case 'Văn nghệ':
        return pink;
      default:
        return orange;
    }
  }
}
